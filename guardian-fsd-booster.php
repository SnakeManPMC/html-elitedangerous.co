<h2>Guardian FSD Booster</h2>
<p>
This module gives your ship 10.5 light year jump range Booster.
</p>

<p>
Tutorials: <a href="https://youtu.be/J9C9a00-rkQ?t=34" target="_blank">Tutorial: Unlocking the Guardian FSD Booster CMDR Exigeous</a> or <a href="https://forums.frontier.co.uk/showthread.php/438289-Walkthrough-Guardian-Module-Blueprints" target="_blank">fdev forum topic: Walkthrough-Guardian-Module-Blueprints</a>
</p>

<p>
Required ship:
</p>
<ul>
<li>A good jump range (you'll be traveling nearly 1000 ly)</li>
<li>Advanced discovery scanner (system you go to is unexplored)</li>
<li>SRV with plenty of ammo, repair and refuel synthesis</li>
<li>8 tons of cargo space</li>
<li>Point defence on the top utility mounts</li>
</ul>

<p>
Required materials:
</p>
<ul>
<li>1x Guardian Module Blueprint Segment</li>
<li>8x HN Shock Mount</li>
<li>21x Guardian Power Cell</li>
<li>21x Guardian Technology Component</li>
<li>24x Focus Crystals</li>
</ul>

<p>
Travel to HD 63154 B 3 A. Lock the guardian structure once it shows up in the navigation panel. Land to the guardian structure. Or you could go to synuefe NL-N C23-4 B 3.
</p>

<p>
Find the six (6) guardian pylons which emerge from the ground as you approach. Activate the two primary pylons to enable the other four.
</p>

<p>
Guardian sentinels arrive, these are the flying alien things which start to shoot at you. You need to destroy these sentinels.
</p>

<p>
Fire at the pylon blue orb at the center to charge them.
</p>

<p>
Once you have charged all the six pylons you need to find an ancient relic. It is located on top of some pylon(???). Shoot the relic at the top of the pylon. When it falls to the ground, just scoop it up to your cargo bay.
</p>

<p>
Go to the machine at head of the site and drop the relic there.
</p>

<p>
Then a large orb appears, scan this orb to receive the guardian blueprints.
</p>

<p>
You get the materials by targeting the destructible panels on some of the pylons, shoot them and pick up the items dropped into your cargo bay.
</p>

<p>
You don't need to pickup scrap coming from the destroyed sentinels for FSD Booster, but it doesn't hurt either because you might have use for that stuff in the future.
</p>

<p>
Now leave this planet and head to populated space to gather remaining materials.
</p>

<p>
Focus crystals can be get from ships after you destroy them or if stupid combat is not your thing as it isnt for most explorers, get them from material trader.
</p>

<p>
HN Shock Mounts can be purchased from stations, find nearest one with <a href="https://inara.cz/galaxy-commodity/150/" target="_blank">inara</a>
</p>

<p>
Hyades Sector DR-V c2-23 A 5, coordinates 44.818, -31.389 is Dav's Hope. Here is a lot of materials you can collect with an SRV. Get your focus crystals here. <a href="https://i.imgur.com/GVok8C9.jpg" target="_blank">image of Dav's Hope map</a>. How to do this: drive around the place with your SRV and collect materials, when they run out, re-login to the game and they have spawned another set of materials, repeat this loop as many times necessary to get 24 focus crystals. Get ready to be bored, this is extremely tedious and stupid grind, but you as explorer want the gaaaardian FSD booster so you got to do it, heh.
</p>

<p>
Next go to guardian technology broker to unlock the FSD Booster module. Find nearest with <a href="https://inara.cz/" target="_blank">inara</a> and choose galaxy -> systems and stations -> nearest -> technology broker. If you have access, shinrarta dezhra jameson memorial has one.
</p>

<p>
Once you arrive to the station go to starport services -> contacts -> technology broker -> guardian fsd booster
</p>

