	<h2>2019-09-09</h2>

<p>
Woke up today, cooked some food and then it was time to play elite, hunt some missing market data. My todays goal is to play as long as I can, I assume to get bored before tired but I'll really want to test myself that how many stations I can visit in a one day. Honestly I'm surprised how small the yesterdays list is with only 37 stations visited, I consider that extremely low amount.
</p>

<p>
Perhaps I'm confusing that to normal exploration traveling which is one system in 44 seconds, while visiting stations for <a href="eddn.php">EDDN</a> sending is more like 14 minutes per station, as strange as that sounds. It really depends, for example distance from star is what mostly dictates how long one station takes, planetary usually takes more time than outpost in space, but if you have close ones with basically no traveling time, then it can be as quick as 5min or so.
</p>

<p>
