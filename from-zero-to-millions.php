	<h2>From Zero To Millions</h2>

<p>
Started new CMDR account, outfitted sidewinder with cargo racks and anything else from the starter system for as high class modules as I could which was pretty much nothing, I think one upgrade to Frame Shift Drive (FSD).
</p>

<p>
Then started to take missions/contracts from the mission board, courier jobs to deliver data only. Along that route I upgraded some cargo rack so capacity was full for sidewinder module slots. This is my <a href="https://edsy.org/#/L=I100000H4C0S20,,,9p310A4Y10AKA20AZA00Ans20B1U20BH620BWQ10,,03w1003w1002M1002M1002M1002M10,PMC_0Starter_05,PMC022" target="_blank">PMC Starter 5, ID PMC022, EDSY</a> build.
</p>

<p>
Those 20 maybe 22 missions courier jobs got me 775m credit budget. Then I went to Laksak system according to some chat server trade-alert (trading from station to fleet carrier), traded two 16t cargo loads for 30k/ton credit profits and that got me to 1.739m credits budget. So clearly courier job missions while they are "nice", they pay pretty much shit, if you really want to maximize your time to gain credits, immediately upon a new CMDR creation max out cargo racks and head over to closest fleet carrier buying colonization commodities with 30k/ton profit or higher.
</p>

<p>
Please note that this applies for spring of 2025 Elite Dangerous v4.1.2.101 game engine mechanics and community happenings, this could and most likely will change in the future. Even if all fleet carrier owner CMDR's get bored for system colonization or finish what they wanted to achieve colonizing, then all those insane 30k/ton or more profit trade adverts go away and profits settle back to the 10k/ton or so.
</p>

<p>
Then I did check for <a href="https://edsy.org/#/L=I200000H4C0S00,,,9p300A7200AMg00AcJ00Aqq00B3_00BK400Bb600,,07200072000720003w0003w0003w0002M0002M00" target="_blank">A rated cobra mk3 trader on EDSY</a> and was shocked to realize its 5.49m credits price. Guess I'll be doing few more cargo runs in 16t sidewinder <img src="../images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

<p>
Next step up from A rated cobra mk3 is A rated lakon type-7 which costs 34.4m credits, see <a href="https://edsy.org/#/L=IX00000H4C0S00,,,9p300A8c00AOE00Adt00AsO00B5Y00BK400Bcg00,,0AA000AA000AA0008c0008c0008c0005U0005U0003w0002M00" target="_blank">A rated lakon type-7 trader EDSY</a>.
</p>

<p>
Was going to purchase lakon type-7 as usual, had the money saved for it and all, but then on the last minute on shipyard menu I saw the new type-8 so decided to give it a try even though I didn't have enough money for all the internal modules. Here is the <a href="https://edsy.org/#/L=Ib00000H4C0S20,,,9p310A9E10AOs20AeB20Aq_20B6A20BKE20Bcg10,,0Bk100AA100AA1007210mpT08c100721003w1002M10,PMC_0Trader_010,PMC024" target="_blank">unfinished lakon type-8 trader on EDSY</a>. Please note that this is unfinished build and should not used as an example what to buy.
</p>

<p>
Here is the <a href="https://edsy.org/#/L=IX00000H4C0S20,,,9p310A5U10AOE00Adt00AsO00B5Y00BK400Bcg10,,0AA100AA100AA1008c1008c1008c1005U1005U1003w1002M10,PMC_0Trader_010,PMC024" target="_blank">A rated lakon type-7 trader on EDSY</a>. Another tweak <a href="https://edsy.org/#/L=IX00000I0C0S20,,,9p310A5y10AOE00Adt00AsO00B6000BK400Bcg10,,0AA100AA100AA1008c1008c1008c1005U1005U1003w1002M10,PMC_0Trader_013,PMC035" target="_blank">EDSY PMC Trader 13 Lakon Type-7</a>.
</p>

<p>
Here is the <a href="https://edsy.org/#/L=IY00000H4C0S00,,,9p300AAA00ARM00AfR00Aty00B8g00BLe00BeE00,,0DI000DI000Bk000AA0008c00072000720005U0005U0003w0002M00" target="_blank">A rated lakon type-9 trader on EDSY</a>.
</p>

<p>
Once you reach Lakon Type-9 Heavy with its awesome 790t cargo capacity, the profits at first feel and are great, however once you continue looking at the horizon for example buying fleet carrier with 5 billion credits, the amount of work you have to do, the cargo to haul, is insane going from Lakon Type-9 Heavy with few dozen million credits to over 5 billion credits. You can go from starter sidewinder + 250k credits to Lakon Type-9 Heavy in one day if you are not making any mistakes, but going from Lakon Type-9 Heavy + lets say 10 million credits to 5 billion credits takes... days, definitely days, dunno if you could do it in a week, maybe, depends completely how profitable fleet carrier trade adverts you find and how many of those you get to do without too many other community CMDR's join to eat those profits. Going from starter sidewinder to type-9 is childs play, not even a challenge, its super fun, but going from type-9 to fleet carrier is pure torture <img src="../images/smileys/icon_surprised.gif" alt="Smiley :o" loading="lazy">
</p>


	<h2>No Shortcuts</h2>

<p>
On my 2025-05-17 game-play with CMDR Snake Man 8th I first short cut my ship upgrade from sidewinder to cobra mk3, I used the default outfitting just slamming it full of maxed out cargo racks, I did the same with cobra mk3 to lakon type-7, however here I ran into problems... lakon type-7 default frame shift drive and fully laden with cargo is a bad mix, hyper space jump range was something like 7.53LY if I remember correctly, that is pathetic. I had to discard 310t of cargo because I could not travel to my destination which was really close by just because distances between star systems were longer than 7.52LY, no amount of economical route plotting helped on that.
</p>

<p>
Never ever take short cuts when upgrading a cargo trading ship! <img src="../images/smileys/icon_sad.gif" alt="Smiley :(" loading="lazy">
</p>

