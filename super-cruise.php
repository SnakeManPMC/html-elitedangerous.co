<p>If you overshoot (happens to all of us when distracted), just throttle back, push your ships nose down (joustick forward) until the destination is about above you, then pull back joystick until the destination is again in front of you.</p>

<p><b>(Old beta way)</b><br>When you are far away keep 100% throttle. When you get 250ls drop down to a 50% throttle and push just little bit more throttle so its not too slow. Drop out of super cruise when your speed is less than 1000km/h and distance to destination (space station) less than 1000km.</p>

<p>In general terms, keep super cruise speed value 10% of the distance to the destination.</p>

<p>If you drop out of super cruise et last minute (using no throttle, 30km/s speeds to get as close to the station as possible), you risk the chance of appearing inside the space station walls therefore killing you instantly, or in best case scenario get wanted status for yourself to enter the station without docking permission.</p>
