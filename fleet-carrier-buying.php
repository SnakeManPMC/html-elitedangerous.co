<p>
Here is example from starting location Sol, <a href="https://inara.cz/elite/nearest-stations/?formbrief=1&ps1=Sol&pi13=&pi14=0&pi15=0&pi16=&pi1=0&pi18=0&pi19=0&pi17=0&pa1%5B%5D=28&ps2=&pi25=0&pi8=&pi9=0&pi26=0&pi3=&pi4=0&pi5=0&pi7=0&pi23=0&pi6=0&ps3=&pi24=0" target="_blank">inara nearest station, fleet carrier vendor</a>.
</p>

<p>
Important bit is the <b>Fleet Carrier Vendor</b>, that is the system/station where you purchase fleet carrier.
</p>

<p>
Fleet carrier cost 5 billion credits, there are no other fees associated with the purchase.
</p>

<p>
When you purchase fleet carrier it is shipped with 500 tons of complimentary <b>Tritium Fuel</b>. This amount of fuel is plenty enough for you to travel somewhere, even few places in the populated space (the bubble). However thinking about the long run you should study where to buy tritium commodity so you can refuel your carrier when tritium fuel is running low. Search inara for <a href="https://inara.cz/elite/commodities/?formbrief=1&pi1=1&pa1%5B%5D=10269&ps1=Sol&pi10=1&pi11=0&pi3=3&pi9=0&pi4=0&pi14=0&pi5=24&pi12=0&pi7=5000&pi8=1&pi13=1" target="_blank">station cheapest tritium with 5k tons supply</a>.
</p>

<p>
Once purchase is complete your brand new fleet carrier will be located in the system or one of the nearby systems if the fleet carrier vendor system is already too busy with other carriers. If your fleet carrier will be placed in nearby systems, there is a good chance it might be "lagged" and not show up right away, it will show up on galaxy map as icon over the system its placed, but once you travel there you cannot find your fleet carrier from either left side navigation panel or system map, this can be fixed by simply "intra system" hyper jumping your carrier which refreshes its location in the system (map) and in your navigation equipment (buggy FDEV servers are to blame). Please note that "intra system" hyper space jump costs something like 5 tons of tritium fuel, if you want to save fuel and time you could try completely shutting down elite and starting it back up again, sometimes this works, othertimese not, it truly seems to depend on FDEV servers and luck.
</p>

