	<h2>Epic Journey Of Exploration Around The Milky Way Galaxy</h2>

<p><b>PMC Exploration Galaxy Loop</b></p>

<p>After reaching Triple Elite status, switch to a relax mode in elite universe and go to an great long journey exploring around the galaxy. Loop clockwise at the "middle" part of galaxy (25,500ly from core) all the way through to the other side of galaxy (from Sol), then get as far in the edge of galaxy as you can jump with current jump capability. From there its so cool to look back at Sol and see the stupid long distance. When done marveling the galaxy, return to the loop route and return to Sol.
</p>

<p>Relaxed exploration, no hurry at all. <b>Scan all interesting looking planets</b>, scan all stars, skip useless belts / icy / rock stuff of course... except with Elite v2.0 you can land on icy and rock planets, so they are no again interesting hehe. Try to scan the galaxy area where youre at for interesting stars. Just enjoy the journey and don't be bothered to count how many light years did you jump today etc.
</p>

<p>My path to triple elite showed in exploration part how easy it is to burn out by playing too much. Don't play every day unless huge enthusiasm strikes. It is very important not to burn out playing too much, trying to jump too many systems at one session / day etc. Try to relax, enjoy the galaxy, if you don't feel like playing then <b>don't</b>. Simple.
</p>

<p>In-game chat Athanasius said that its roughly 2 * 25500 * 3.14 == 160,140Ly journey <img src="../images/smileys/icon_wink.gif" alt="Smiley ;)" loading="lazy">
</p>

<p>If we calculate some useless statistics: 160,140Ly / 2000 (60min of jumping is two thousand Ly) == 80.07 hours. 80.07 / 24 == 3.33 days.
<br><br>
But bit more practical formula would be: 80.07 / 12 hours game time per day == 6.67 days. But yeah as you can understand its completely useless to try to calculate these as you have to count how much time you spend in one system, if the system map is empty of anything interesting then its 0 seconds per jump as you check it while FSD is charging... however if there are something interesting, then you can spend even half an HOUR in a system. Totally useless trying to estimate these into some formula.
<br><br>
The point of PMC Galaxy Loop is to enjoy the exploration, simple.
</p>

	<h2>PMC Galaxy Loop Doctrine</h2>

<p>Doctrine how to explore while in PMC Galaxy Loop journey. First and foremost it should be an enjoyable relaxing adventure where you take the time to appreciate the beauty and majestic sights of the galaxy. Do not rush this, it is not a idiotic dancing & frag race.
</p>

<p>When route is plotted, walk it through on galaxy map so you can see if you go by any interesting stars or too many non fuel scoop compatible stars etc. Also in galaxy map scan the nearby areas every once in a while, maybe every 500Ly or so, you might find black holes or other interesting stuff. Think about using economic route plotting sometimes.
</p>

<p>After dropping out of hyper space into a new star system:
</p>
<ul>
<li>Advance discovery scan (doh! obviously!)</li>
<li>Surface scan the main star to leave your first discovery mark</li>
<li>Surface scan neutron stars, black holes, ammonia, water and earth like worlds regardless of distance</li>
<li>Surface scan all planets with atmosphere that look interesting up to 10,000ls distance (or if there are many orbiting a distant star)</li>
<li>Check out small / large landable planets from system map</li>
<li>Land on extremely interesting looking planets, or if you just feel like it</li>
</ul>

<p><b>When approaching landable planet</b><br>
When detailed surface scanner start to scan, cut throttle and wait. When its done check system map for planets gravity, if its over 4g you are not "allowed" to land there (in risk of destroying your ship).
</p>

