	<h2>Questions And Answers</h2>

<p>
Frontier Developments questions and answers about system colonization.
</p>

<p>
it costs credits to claim a system
</p>

<p>
after system claim you have four (4) weeks (ie one month) time to haul the materials for the system colonization ship
</p>

<p>
claiming a system is first come first served
</p>

<p>
player can be system architect as many systems as they want to
</p>

<p>
player can claim only one system at the time, so no hoarding of systems
</p>

<p>
if you fail to claim a system TWICE (2), you will be locked out from the claiming process for <i>"number of days"</i>
</p>

<p>
claims can only be bought from THE BUBBLE, so not from colonia or explorers anchorage etc
</p>

<p>
system colonization claim radius is 10 light years
</p>

<p>
system architect CMDR name will be displayed in the system
</p>

<p>
can system colonization change existing systems infrastructure: no, it cannot
</p>

<p>
system map shows some possible colonization options/features/possibilities on explored systems
</p>

<p>
system colonization is not a player versus player (pvp) feature, you cannot attack colonized systems or blockade them etc
</p>

