	<h2>Frontier Unlocked Highlight Colonization</h2>

<p>
You purchase system colonization claim for credits (unknown amount before feb 26th when beta begins). Once you do that you'll receive colonization navigation beacon. Now you have 24 hours time to travel into the claimed system and deploy that colonization beacon. If you fail to deploy the colonization navigation beacon within 24hrs then system claim is void, its no longer active, anyone can claim the system at that point.
</p>

<p>
You can be able to select your primary starport. This will be space port and LOCATION pre-determined by the target system.
</p>

<p>
Next up a colonization ship will arrive. This is a very big ship, its probably one of the biggest ships in-game. Colonization ship has multiple large landing pads.
</p>

<p>
You as system architect, CMDR who made the claim and any others willing to help, need to deliver commodities, materials to the colonization ship in order to build the primary starport. These materials vary depending on what (starport) you're building. System colonization ship will have a LIST OF MATERIALS it needs, once this list of materials have been delivered, the new start port will be deployed.
</p>

<p>
A new start port will be deployed and initially it will be under construction (under deployment), not all services functioning. On the next THURSDAY weekday (0700hrs UTC Elite Dangerous server maintenance period) your new starport will be deployed and its fully accessible.
</p>

<p>
Once starport have been built and deployed, colonization ship remains in the system <i>"for a while"</i>, before it leaves.
</p>

<p>
Two things will happen when primary starport has been constructed and you must complete these in period of time as well: colonization navigation beacon becomes a proper standard navigation beacon and system contact appears on the starport. This new system contact allows you to make claims to another 10 light years from this point to any available unclaimed system.
</p>

<p>
When you explore a system you will see <i>"how many slots"</i> of system colonization options for outposts etc a system has.
</p>

<p>
Power play: once primary starport is completed, the system is NOT controlled by a power. The faction you bought the system claim for will be brought over to this system/star-port.
</p>

<p>
Once primary starport has been completed you will see from system map all possible locations for facilities for this system. You can select a location and then able to choose a facility that is appropriate for the location. This new location again launches the construction phase where you must deliver construction materials to this newly chosen location and facility. Once construction is finished that new facility will deploy.
</p>

<p>
You must build/construct <i>"smaller tier"</i> facilities first before you get to build the really big starports for example. This happens on both space ports and planet surfaces.
</p>

<p>
Constructing a planetary outpost is different than a space port. You have to pick your surface facility location on the planets surface. Once done, the normal building/constructing phase begins where you must deliver materials for the construction to complete.
</p>

<p>
If you're building a mining facility then system economy becomes a mining economy. So if you're path finding a long way into the deep space in Milky Way galaxy to build your ultimate home system, you can come back to these specific economy systems you already colonized to pickup those raw materials for the building process, you don't have to travel all the way back to the bubble or other populated places (colonia etc) to get resources, just use your own or other CMDR colonized systems, as long as the economy matches so you'll find the resource materials you need there.
</p>

