<p>
2020-06-19T21:13:24Z Back in PMC Explorer asp cockpit on board carrier PMC Wanderer, in system: <a href="https://www.edsm.net/en/system/id/53336662/name/Dryaea+Brou+TS-U+e2-0" target="_blank">Dryaea Brou TS-U e2-0</a>.
