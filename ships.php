<h2>Ship trade-in values (premium beta era info)</h2>

<p>For the purposes of testing the Eagle had no guns or modules fitted and was at 100% hull.</p>

<p>Credits before transaction = 122,681<br>
Cobra Cost 111,766<br>
Credits after transaction = 47,851<br>
Minus the expected amount that would have remained (10,915)<br><br>

That gives us our trade in value of 36,936<br>
Eagle cost brand new = 41,040<br><br>

So to work out the percentage 36,936 / 41,040 = 0.9 or 90%<br><br>

Ship insurance: Rates of 5% to 10% are going around, so at a price of just over one million that would be 50,000 to 100,000 credits for insurance alone, that is without weapons, with weapons you'll have to add the same rates to the buying price of the weapon.</p>

