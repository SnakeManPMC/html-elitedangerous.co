<h2>Mining</h2>

<h2>How to mine astroid fields and rings</h2>

<p>Mining tips: Metal Rich will give you things like Coltan and Aites, but Metallic Rich will give you Painite, Platinum, Palladium, Gold, Silver, etc.
</p>

<p>Painite from HIP 4005. I've only ever found Painite in Metallic Rings that are Pristine. In rings you have to pick the right section, I usually descent into the smallest and darkest rings and that seems to work. Make sure you are in the correct ring too. Many planets have 2 or 3 rings. A ring is closest to the planet, B the next one out and C being farthest away. If they are different type of ring, they will be different colours.
</p>

<p>If you cant find good minerals you are probably just outside the inner ring (it's quite thin). Theres a rocky outer ring and a small metallic inner ring.
</p>

<p>Pristine Reserves Metallic ring systems: <a href="pristine-reserves-metallic-rings.php">Pristine Reserves Metallic Rings</a>, or image link <a href="http://imgur.com/a/vEqgv">imgur: Elite Dangerous Pristine Metallic Mining Sites</a>
</p>

<p>If you go to a <a href="resource-extraction-sites.php">Resource Extration Site (RES)</a>, you might want to move further than 30km from the navigation marker so the AI ships stop spawning around you and therefore possibly attacking you. You can also pick ANY spot from the rings to drop out of supercruise and start mining without AI presence. In v1.4 however it seems to spawn one set of AI ships when you drop out of supercruise but that is all, if you evade/destroy those ships you're left alone.
</p>

<p>Asteroid belts are not worth mining, go to planet rings instead(?)
</p>

<p>Concentrate mining on "the tree P's", Painite, Platinum and Palladium in this order of values, painite being the most expensive actually twice as expensive as platinum. Do not bother to mine rest of the stuff as its like three times cheaper than three P's.
</p>

<p>Get two small or medium mining lasers and 4A refinery. Minimum of two collector limpet controllers and one prospector limpet controller. Don't forget to purchase limpets from stations munitions menu as limpet controllers are useless without limpets.
</p>

<p>Even if your cargo hold is full you can still store stuff in the refinery bins, once you are in station and sell your cargo you can move the stuff from refinery bins into cargo.
</p>

<p><a href="http://www.edshipyard.com/#/L=705,5TP5TP5TPH9WH9W0_g0_g0_g0_g,2-9Y9Y7_6uB6A08I,0AA0AA7Uc5Vk5Vk4_w0aQ05U5uk">Suggested python outfitting</a> in edshipyard.
</p>

<p>If you have already picked up tons and someone is coming in for a scan, turn toward them so you are ready to fight when they engage and don't compromise your collectors flight path. As much to shields as you can manage.
</p>

<p>High Tech/Refinery systems seem to be the best place to sell your mined metals. If you can find bulletin board missions which buy minerals they pay great deal for these, however you need to judge if your time is worth to use searching for these missions or instead just selling the stuff on commodity market.
</p>

