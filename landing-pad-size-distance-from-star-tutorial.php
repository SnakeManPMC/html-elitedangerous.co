<h2>What is landing pad size?</h2>

<p>It means that large ships cannot land on Small or Medium sized pads, they only land / dock on Large landing pads.</p>

<p>Ships using landing pads - Large:<br>
Imperial Clipper<br>
Lakon Type-7<br>
Anaconda<br>
Lakon Type-9</p>

<p>Ships using landing pads - Medium:<br>
Python<br>
... and all the rest of the smaller ships in Elite Dangerous v1.2.07 release. BTW you should not worry about Small pads.</p>

<h2>How do I know which pad size a particular station has?</h2>

<p>Check station logo from System Map and compare to this <a href="http://imgur.com/kYMy1BB">ugly station landing pad size legend</a>. Simply to put: outposts have medium (small + medium) size pads and starports have large (small + medium + large) size pads.</p>

<h2>What is distance from star?</h2>

<p>It is how far in light seconds (Ls), a specific station is from the systems main star (where you drop out of hyperspace to).</p>

<h2>How do I find stations distance from star?</h2>

<p>Go to System Map either through Galaxy Map, choose system and click System View (it should be system MAP) or from cockpit left hand side navigation panel select System Map and find your station. Then click it to see "Arrival Point", which value is what you enter into station "Distance" field. Do not use decimals like 100.12Ls, just type in 100.</p>

<h2>Can I use cockpit left hand side Navigation panel to see Distance?</h2>

<p>Umm no, because it shows the distance of where YOU are, not the real distance from star. If you are next to the star, yes then it works, but its much easier and unified for everyone to just check System Map for Arrival Point value.</p>

<h2>Why is landing pad size important?</h2>

<p>When you fly small ships it isn't but when you fly large ships then you cannot land / dock into outposts which means lot of markets are unavailable to you. If trading tools would not make distinction between landing pad sizes then they would instruct your large ship to go dock into small outpost where you cannot dock.</p>

<h2>Why is distance from star important?</h2>

<p>If you want to be efficient in trading you need to filter out stations that are much further than 1000Ls from the star. I mean would you like to run trade route where you travel to Hutton Orbital 1,200,000 light seconds away spending 1h 30min in supercruise? Yep I thought you wouldn't :)</p>
