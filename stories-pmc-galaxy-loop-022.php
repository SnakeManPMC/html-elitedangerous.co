	<h2>Conclusion</h2>

<p>
2017-12-05
</p>

<p>Many things happened and changed while I was on PMC Galaxy Loop exploration journey. Journey started with the mood of using economy route plotting and just relaxing, but it ended up with me a bit bored of exploring and just using max jump range only to stop detail surface scan ammonia, earth like or water worlds.
</p>

<p>Actually more than half of this trip basically starting even before reaching beagle point was done with that high speed jump as quickly as you can attitude. For example right now I can only remember couple of times I landed on planet to enjoy the view and mood, on PMC Galaxy Loop videos there will be more details.
</p>

<p>However mostly lack of landing on planets was because of the landing without shields causing damage bug and the fact that I'll get motion sickness driving that stupid SRV (32" monitor close to my eyes and TrackIR 5 with pro clip), even the options to disable HMD motion sickness do not help me. Only thing that helps is to turn on all lights in my room and sit far far back in my computer desk, but that is just stupid way to do any computer stuff. Of course you chould just fly around in your ASP above the surfaces, but that somehow is kind of like... why bother if you don't go full boots on ground so to speak. Anyways.
</p>

<p>I took a break from Elite for hardcore ArmA 3 terrain editing which ate like 11 months, so saying PMC Galaxy Loop took almost a year would be a bit false as the actual in-game hands on cockpit time was mere weeks if you would compress that to 16hrs (heck even 12hrs) elite playing days. The formula I calculated at beginning of this page shows that you could, in theory, do this journey in one week with 12hrs a day every day jumping, which obviously is not really possible for exploration (detailed surface scans).
</p>

<p>In that sense this exploration journey while I did complete it as planned, was not really done in the manner I had... well, planned. I'm not disappointed or anything, whats done is done, I accomplished this loop around milky way galaxy what I said I would do and that's all that matters. As result I'll most likely avoid making such long term exploration plans in the future as it feels silly to set into such a long journey with specific mindset only to return with completely other heh.
</p>

<p>Was this fun? memorable experience? Yes it was. Do I plan another this scale exploration journey soon? Hell no! <img src="../images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

