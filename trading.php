<h2>Jump Timing</h2>
<p>Trading runs in Lakon Type-9 cargo ship in perfect conditions (no toilet break, no telephone, no food, no family, no nothing but 100% Elite concentration), how long it takes to depart, hyper jump and dock at destination. These are my test results.
</p>
<p>Departure from station, 4 jumps, arrival to station: 7min 15sec and 7min 48sec
<br><br>
Departure from station, 2 jumps, arrival to station: 4min 25sec
<br><br>
Buy commoditys, depart, 2 jumps, arrival, sell commoditys: 5min 12sec and 6min 18sec
</p>
<p>You do have to realize that this is just few trade runs which were timed. It varies so much of what kind of other ship traffic you get when docking and the dropping out of hyper space near the star location and your next jump destination etc. So many variables. However in these timings there were no real life (as listed above) distractions.
</p>

<p>All in all its stupid to list X per hour stuff in elite as so many variables affect it. Just enjoy the game and dont worry what you can manage to do in an hour.
</p>


<h2>Station un-docking</h2>
<p>Un-docking timing from moment of clicking launch to starting charge FSD.
</p>
<p>Station depart cheating: 31sec
<br><br>
Station depart normal: 55sec, 2 x 58sec, 2 x 1min 1sec
</p>

<p>Un-docking cheating means that you select Launch from the ship menu, then immediately hit ESC, then exit to main menu and then start to join back into game. Now you appear about 20km outside the station without mass lock and ready to hyper jump. Some people use this as cheat to bypass the (in their opinion, difficult) un-docking procedures.
</p>

<p>The results are that cheating method is about 30 seconds quicker on ideal conditions (no traffic in docking corridor), however its extremely lame and gamey cheating method and in big picture <b>its not worth the time saved</b>.
</p>

