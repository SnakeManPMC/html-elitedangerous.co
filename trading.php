<h2>Elite Trading Basic Info</h2>
<p>Commodities market is where you buy goods, travel to different system and sell those goods there for profit.
</p>

<p>Back in first Elite at 1984 we used paper and pencil to write down market prices around the star systems, now in Elite Dangerous at 2014 we have 3rd party tools and web sites to aid our trading so we dont have to write anything down on paper.
</p>

<p>Galaxy map's trading routes are really not useful for trading, you should use 3rd party tools and web sites instead.
</p>

<p>What you need is cargo ship equipped with cargo racks and some credits. It all depends on which lever you are starting your trading, like brand new Commander will get more credits from doing bulletin board missions or bounty hunting than trading in sidewinder. However if you must you can start trading in sidewinder too, its just very slow progress. Basically ships like Hauler and Cobra are the ones where you start to make some better profits.
</p>

<p>In Elite v1.4.01 the best trading ship is Lakon Type-9 without shields which gets you 532 tons of cargo space on 12.70Ly jump range.
</p>

<p>So how do you trade? Well if you are sidewinder beginner Commander you should start with <a href="eddb.php">EDDB</a> website which works nicely in browser (obviously) and is easy to use. When you are more advanced in Elite and trading, you have only one tool to turn to and that is <a href="tradedangerous.php">TradeDangerous</a> which is the best trading tool available... the catch is, it uses Python and is command line so unless you know little bit about computers you might have difficulties using it.
</p>

<p>Most important thing with low credit balance trading is to always keep your ships Rebuy cost credits before departing from station. Especially if you are beginner and been trading several hours to get into that hauler/cobra etc ship, you somehow get killed, lose all your credits and ship... is very bad, trust me on this; do not depart without rebuy credits.
</p>

