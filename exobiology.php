	<h2>Exobiology</h2>

<p>
What is <b>exobiology</b>? Its like exploration but on foot on planets surfaces. You land on a planet, get out of your ship, on foot, then approach some crap on the ground, scan it with your ARTEMIS suit some scanner thingy, then get back to populated space on VISTA GENOMICS and sell the scan data there. Selling "first footfall" discovery scan data is the most profitable thing to do in Elite Dangerous v4.1.1 release.
</p>

<p>
Where to buy ARTEMIS suit? You need to visit PIONEER SUPPLIES shop in any large station.
</p>

<p>
Normal exploration with a ship is done without shields, no self respecting explorer flies with shields, however when you land on multiple planets, when the whole operations focuses on planetary landings, then unfortunately you do need a shield because Elite v4.1.1 still has the same bug that your ship takes hull damage if you land it even on slightly "off axis" which is impossible to tell with the lack of landing instruments, so get a shield, this is special purpose operation, this is not normal exploration.
</p>

<p>
Your ship needs Detailed Surface Scanner, but then again this is basic exploration outfitting module anyways.
</p>

<p>
On your ship, hit HOTAS joystick thumb down button (cant recall what keyboard it was, the middle center screen look down button), then choose "loadouts", then choose "manage loadouts", then choose "+ new loadout", then choose "select base suit", and now finally choose artemis suit and apply suit. Then save loadout. And I guess "use loadout", then it should be done.
</p>

<p>
Then I walked up to some white crap on the ground, tool/weapon pointer turned into "ready to sample" and then I had to press and hold left mouse button (LMB) until the tool was done sampling.
</p>

<p>
When you walk into your landed ships blue pulsating light cone or whatever its called, that will rearm, or re-energize your artemis suit electric battery.
</p>


