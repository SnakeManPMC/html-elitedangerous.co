	<h2>Fleet Carrier Docking Access</h2>

<p>
Docking access allows settings for: all, friends only, friends and squadron, none.
</p>

<p>
In Elite Dangerous v4.1.0.2 docking access friends and squadrons is bugged, its not working properly. Its recommended to use "friends only" if you want to limit access to public at large.
</p>


	<h2>All</h2>

<p>
Everyone is allowed to dock. Please note that if you additionally select the notorious CMDR status preventing docking permission, then obviously those bad wanted CMDR's cannot dock even with "All" docking access set.
</p>


	<h2>Friends Only</h2>

<p>
This means that any CMDR who wants to dock to your fleet carrier must be in your Elite Dangerous in-game friends list.
</p>

<p>
If you have CMDR's who are not in your friends list docked in your fleet carrier and then you set docking access to Friends Only, they will be moved off from the carrier, for example if you set docking access to friends only and then hyper space jump carrier away from the system, the CMDR who was not in your friends list and docked will be left in the hyper space jump origin system.
</p>


	<h2>Friends And Squadron</h2>

<p>
<b>Friends and Squadron docking access is not working, please do not use it.</b>
</p>

<p>
If one day Frontier Developments would fix this bugged feature, it would then allow all CMDR's in your friends list and Squadron members to dock. This is much more relaxed docking access, it easily allows your whole squadron members list to dock.
</p>


	<h2>None</h2>

<p>
No CMDR can dock.
</p>

