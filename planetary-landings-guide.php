<section>
<p>Basic info you need to know to set down on those planets. Planetary Landings is called Elite Horizons, otherwise known as Elite Dangerous v2.0 and also known as Season 2.</p>

<p>If you have purchased only Elite Dangerous v1.0 (upgraded to v1.5 in december 2015), then you cannot land on planets.
</p>
<h2>Orbital Glide</h2>
<p>The glide currently disengages at ~7km above (an approximate) floor height, 11km short of face-firsting into the landscape, or when you come in too steep (60 degrees plus).
</p>

<h2>Planetary Ship Scanner</h2>
<p>In the ship a blue circle on your scanner will indicate a nearby point of interest.
</p>

<h2>Ship Heads Up Display (HUD) Indicators</h2>
<p>Ship HUD indicators: OC is orbital cruise, DRP is drop (out of orbital cruise). Terrain scanner appears below 75m altitude.
</p>

<h2>SRV Wave Scanner</h2>
<p>In SRV use wave scanner to find mineral rocks called "outcrop", target them and shoot them with the turret gun. Open cargo scoop, target the mineral object and drive over it to scoop it onboard.
</p>

<h2>SRV Docking to Outpost</h2>
<p>When you have docked your ship into planetary surface outpost and departed from the ship with your SRV, in order to get back to your ship you have to request docking from the outpost. At that point your compass will point you towards the hangar where your ship is located. Drive to the hangar all the way in and then you can use Board Ship command from the center UI menu.
</p>

<h2>Point Of Interest (POI's)</h2>
<p>Point Of Interest (POI's) on planet surfaces. POIs can only be seen from your ships (or SRVs) normal scanner, they can not be see from super or orbital cruises. When you are in normal space flying mode, increase the scanner range to see further out. POIs show up in the scanner as blue (or perhaps purple'ish?) colored circle areas. In SRV you can track POIs using the wave scanner and finally when youre close enough, the normal scanner.
</p>

<h2>Settlements</h2>
<p>Navigation panel settlement name ++ marker means its medium size, with perhaps 3-5 data points. Low security means probably no tresspass zones for the SRV, but does have no fly zone for ships. One plus sign is small.
</p>

<p>You can improve your SRV's fuel efficiency by driving sensibly, not using boost and turning off modules if they're not needed.
</p>

<h2>Planetary Landings Exploring</h2>
<p>It is very easy to destroy your ship when crashing into terrain. So if you want to fly in those canyons fighter jet style with adrenaline in your veins... you have only yourself to blame when your ship gets destroyed 30,000Ly from populated space.
</p>

<p>Always, always check planets gravity. The larger the planet the more gravity it has, you should not try to land on planets while exploring that have more than 4g gravity as it is extremely dangerous. You can easily destroy your ship as it sinks like a brick when landing.
</p>

<p>Be very very careful while exploring and landing on planets.
</p>

<h2>Planetary Ports / Outposts</h2>
<p>When you are in a ship landing into planetary settlement / outpost, be very very careful with the approach as there is gravity involved. It is not the same as docking your ship in space, you sink like crazy in the planets depending on the planets gravity.
</p>

