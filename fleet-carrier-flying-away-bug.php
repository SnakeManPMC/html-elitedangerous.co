	<h2>Fleet Carrier Flying Away Bug</h2>

<p>
You super cruise to your fleet carrier orbiting a planet with nearby other facilities like outposts and starports, you drop out of super cruise... and fleet carrier is flying away from you so that you are unable to catch it for docking, what the hell is this?
</p>

<p>
This is the notorious fleet carrier flying away bug. It happens if your fleet carrier is orbiting a planetary body with a starport and possibly other outposts etc facilities, the carrier somehow synchronizes itself with the orbit of the facility instead of just remaining stationary, this makes if move really fast in normal space creating the illusion that fleet carrier is flying away from you. Who knows what the true technical explanation to this is, some people say its players ship being instanced with the station which makes fleet carrier move in normal space, no idea what is true, I can only tell you what the effects are in-game ie flying away and in which conditions it happens.
</p>

