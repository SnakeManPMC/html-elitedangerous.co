</p>

<p>
How do you avoid fleet carrier flying away from you bug? Well, simple answer is you cant. But you can reduce the chance of this bug appearing by not parking your fleet carrier orbiting a planetary body with multiple facilities. In my example there was a coriolis starport and two outposts, not sure if the planets radius is any indicator about orbits I think planetary orbit is its own thing regardless off object size but then again I'm no astronomer, just guessing here. If you know a way to avoid this bug, like scientific way to prevent it like choosing a fleet carrier parking spot, please let me know by posting to PMC Tactical Forum.
